export const CERTIFICATE_CODES = ["STC", "ESC", "VEEC", "PRC", "LGC", "ACCU", "SMC"] as const;

export type CertificateCode = (typeof CERTIFICATE_CODES)[number];

export interface CertificateDefinition {
  code: CertificateCode;
  name: string;
  region: string;
  relevance: "Household upgrades" | "Broader energy market";
  plainEnglish: string;
  represents: string;
  whyPriceMatters: string;
  officialUrl: string;
  colour: string;
}

export interface CertificateTrade {
  code: CertificateCode;
  tradedOn: string;
  priceCents: number;
}

export interface CertificatePricePoint {
  tradedOn: string;
  priceCents: number;
}

export interface CertificatePriceSeries extends CertificateDefinition {
  latest: CertificatePricePoint | null;
  points: CertificatePricePoint[];
}

export interface CertificatePriceDataset {
  asOf: string;
  source: {
    name: string;
    url: string;
    lastCheckedAt: string;
    status: "current" | "stale";
    note: string;
  };
  certificates: CertificatePriceSeries[];
}

