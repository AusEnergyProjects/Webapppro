const CERTIFICATE_CODE_SET = new Set<string>(CERTIFICATE_CODES);
const SOURCE_SERIES_PATTERN = /type:\s*["']scatter["'][\s\S]*?dataPoints:\s*(\[[^\]]*\])[\s\S]*?legendText:\s*["'](ESC|VEEC|STC|LGC|ACCU|PRC|SMC)["']/g;

function isCertificateCode(value: string): value is CertificateCode {
  return CERTIFICATE_CODE_SET.has(value);
}

function isoDateFromTimestamp(value: number): string {
  if (!Number.isFinite(value) || value < Date.UTC(2000, 0, 1) || value > Date.UTC(2100, 0, 1)) {
    throw new Error("The certificate source contained an invalid trade date.");
  }
  return new Date(value).toISOString().slice(0, 10);
}

export function parseDemandManagerCertificateHtml(html: string): CertificateTrade[] {
  if (!html || html.length > 3_000_000) throw new Error("The certificate source response was invalid.");

  const trades: CertificateTrade[] = [];
  const found = new Set<CertificateCode>();
  for (const match of html.matchAll(SOURCE_SERIES_PATTERN)) {
    const code = match[2];
    if (!isCertificateCode(code)) continue;
    let points: unknown;
    try {
      points = JSON.parse(match[1]);
    } catch {
      throw new Error(`The ${code} certificate history could not be read.`);
    }
    if (!Array.isArray(points) || points.length === 0) throw new Error(`The ${code} certificate history was empty.`);
    for (const point of points) {
      const x = Number((point as { x?: unknown }).x);
      const y = Number((point as { y?: unknown }).y);
      if (!Number.isFinite(y) || y <= 0 || y > 10_000) throw new Error(`The ${code} certificate history contained an invalid price.`);
      trades.push({ code, tradedOn: isoDateFromTimestamp(x), priceCents: Math.round(y * 100) });
    }
    found.add(code);
  }

  const missing = CERTIFICATE_CODES.filter((code) => !found.has(code));
  if (missing.length) throw new Error(`The certificate source did not include ${missing.join(", ")}.`);
  return trades;
}

export function collapseCertificateTradesByDay(trades: CertificateTrade[]): CertificateTrade[] {
  const daily = new Map<string, CertificateTrade>();
  for (const trade of trades) daily.set(`${trade.code}:${trade.tradedOn}`, trade);
  return [...daily.values()].sort((left, right) => left.code.localeCompare(right.code) || left.tradedOn.localeCompare(right.tradedOn));
}

export function isoDateMonthsBefore(date: Date, months: number): string {
  const firstOfTargetMonth = new Date(Date.UTC(date.getUTCFullYear(), date.getUTCMonth() - months, 1));
  const lastDayOfTargetMonth = new Date(Date.UTC(firstOfTargetMonth.getUTCFullYear(), firstOfTargetMonth.getUTCMonth() + 1, 0)).getUTCDate();
  const result = new Date(Date.UTC(firstOfTargetMonth.getUTCFullYear(), firstOfTargetMonth.getUTCMonth(), Math.min(date.getUTCDate(), lastDayOfTargetMonth)));
  return result.toISOString().slice(0, 10);
}
